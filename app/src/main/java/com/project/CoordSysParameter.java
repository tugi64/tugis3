package com.project;

public class CoordSysParameter {
    private int iconResId;
    private String name;

    public CoordSysParameter(int iconResId, String name) {
        this.iconResId = iconResId;
        this.name = name;
    }

    public int getIconResId() {
        return iconResId;
    }

    public String getName() {
        return name;
    }
}

